START
     ENTRY
	 MOV R1,#04
OUTER MOV R4,R1
     LDR R0,=0x00008000
	 LDR R8,=0x00009000
BACK LDR R2,[R0]
     LDR R3,[R0,#04]
	 CMP R2,R3
	 BHI NEXT
	 STR R2,[R0]
	 STR R3,[R2,#-04]
NEXT SUBS R4,R4,#01
     BNE BACK
	 SUBS R1,R1,#01
	 BNE OUTER
AGAIN B AGAIN
     END